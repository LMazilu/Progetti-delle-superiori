    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String s = ricerca.getText().toLowerCase();
        int x = jTable1.getColumnCount();
        int y = jTable1.getRowCount();
        int k = 0;
        int stato = 0;
        try {
            f = new RandomAccessFile("src/files/Canzoni.csv", "r");
            Boolean[] contains = new Boolean[y];
            int stato2 = 0;
            String r = f.readLine();
            String[] a = new String[20];
            for (int i = 0; r != null; i++) {
                contains[i] = r.toLowerCase().contains(s);
                if (contains[i]) {
                    stato2 = 1;
                    if (stato == 0) {
                        clearTable(jTable1);
                        stato = 1;
                    }
                    for (int c = 0; c < 6; c++) {
                        a = r.split(";");
                        for (int j = 0; j < x; j++) {
                            jTable1.setValueAt(a[j], k, j);
                        }

                    }
                    k++;

                }
                r = f.readLine();
            }

            if (stato2 == 0) {
                JOptionPane.showMessageDialog(null, "La ricerca non ha prodotto risultati!", "Attenzione!", JOptionPane.ERROR_MESSAGE);
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(utente.class
                    .getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(utente.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

