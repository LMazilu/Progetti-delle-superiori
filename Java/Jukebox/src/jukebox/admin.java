    public void creaTabella() {
        try {
            f = new RandomAccessFile("src/files/Canzoni.csv", "r");
            f1 = new RandomAccessFile("src/files/Cantanti.csv/", "r");
            f2 = new RandomAccessFile("src/files/Generi.csv", "r");

            String s = f.readLine();
            for (int j = 0; s != null; j++) {
                for (int i = 0; i < 6; i++) {
                    jTable1.setValueAt(s.split(";")[i], j, i);
                }
                s = f.readLine();
            }
            f.close();
        } catch (IOException ex) {
            Logger.getLogger(admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void aggiunta() {
        try {
            f = new RandomAccessFile("src/files/Canzoni.csv", "rw");
            f1 = new RandomAccessFile("src/files/Cantanti.csv/", "rw");
            f2 = new RandomAccessFile("src/files/Generi.csv", "rw");

            int i = 1;
            String ln = f.readLine();

            while (ln != null) {
                i++;
                ln = f.readLine();
                f1.readLine();
                f2.readLine();
            }

            f.writeBytes(i + ";" + this.jtitolo1.getText() + ";" + this.jartista1.getText() + ";" + this.jdata1.getText() + ";" + this.jalbum1.getText() + ";" + this.jgenere1.getText() + "\r");
            f1.writeBytes(i + ";" + this.jartista1.getText() + "\r");
            f2.writeBytes(i + ";" + this.jgenere1.getText() + "\r");

        } catch (IOException ex) {
            Logger.getLogger(admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

